-- USER 테이블 생성
CREATE TABLE USERS (
    ID VARCHAR2(30),
    PW VARCHAR2(30)NOT NULL,
    PH VARCHAR2(20)NOT NULL
);
ALTER TABLE USERS ADD CONSTRAINT USERS_PK PRIMARY KEY (ID);


-- 분실신고 테이블 생성
CREATE TABLE LOOKFOR (
    KEY_NUM NUMBER(10),
    ANI_TYPE VARCHAR2(30),
    LOST_TIME DATE,
    LOST_AREA VARCHAR2(40),
    TITLE VARCHAR2(50)NOT NULL,
    CONTENT VARCHAR2(1000)
);

ALTER TABLE LOOKFOR ADD CONSTRAINT LOOKFOR_PK PRIMARY KEY (KEY_NUM);


-- 분실신고 댓글 테이블 생성
CREATE TABLE LF_COMMENT (
    CO_NUM NUMBER(10),
    CONTENT VARCHAR2(200),
    CO_DATE DATE
);
ALTER TABLE LF_COMMENT ADD CONSTRAINT LF_COMMENT_PK PRIMARY KEY (CO_NUM);


-- 발견신고 테이블 생성
CREATE TABLE FIND (
    KEY_NUM NUMBER(10),
    ANI_TYPE VARCHAR2(30),
    FIND_TIME DATE,
    FIND_AREA VARCHAR2(40)NOT NULL,
    PH VARCHAR2(20)NOT NULL,
    TITLE VARCHAR2(50)NOT NULL,
    CONTENT VARCHAR2(1000)
);
ALTER TABLE FIND ADD CONSTRAINT FIND_PK PRIMARY KEY (KEY_NUM);


-- 발견신고 댓글 테이블 생성
CREATE TABLE FIND_COMMENT(
    CO_NUM NUMBER(10),
    CONTENT VARCHAR2(200),
    CO_DATE DATE
);
ALTER TABLE FIND_COMMENT ADD CONSTRAINT FIND_COMMENT_PK PRIMARY KEY (CO_NUM);
